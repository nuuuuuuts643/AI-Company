# T2026-0429-B: ストーリー分岐判定 (semantic should_branch)
# 仕様: docs/rules/story-branching-policy.md §3.3 判定マトリクス
#   - 主役エンティティ (PERSON / ORG 上位2件) 重複あり × 因果連続あり → 継続 (False)
#   - 主役エンティティ重複なし → 分岐 (True)
