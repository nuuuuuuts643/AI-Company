        f'視点アクター: {primary_actor} の立場で分析せよ (perspectives と同じ集合から借りる)。\n'
        f'★波及先を必ず特定せよ (候補: {impact_str})。\n'
        '★1 次効果 (誰でも分かる直接影響) は書くな。例: 原油高 → 「物価が上がる」は禁止。\n'
        '★2 次・3 次の連鎖 (政策対応・市場反応・社会変化) まで踏み込め。\n'
